string xor_n(string s1, string s2)
{
	string r;
	for (int i = 0; i < s1.size(); i++)
	{
		if (s1[i] != s2[i])
			r += "1";
		else
			r += "0";
	}
	return r;
}
string bin(int x, string r)
{
	if (x == 0) return"0000";
	int a, b;
	a = x / 2;
	b = x % 2;
	if (a != 0)
		r = bin(a, r);
	r += to_string(b);
	return r;
}
string HextoBit(string hex)
{
	string r;
	hex = hex.erase(0, 2);
	map<string, string> table = { { "0","0000" },{ "1","0001"},{ "2","0010" },{ "3","0011" },{ "4","0100"},{ "5","0101" },
	{ "6","0110" },{ "7","0111" },{ "8","1000" },{ "9","1001" },{ "A","1010"},{ "B","1011"},{ "C","1100" },{ "D","1101"},{ "E","1110"},{ "F","1111" } };
	for (int i = 0; i < hex.size(); i++)
		r += table[hex.substr(i, 1)];
	return r;
}
string BittoHex_lower(string bit)
{
	string r;
	map<string, string> table = { {"0000","0"},{"0001","1"},{"0010","2"},{"0011","3"},{"0100","4"},{"0101","5"},
	{"0110","6"},{"0111","7"},{"1000","8"},{"1001","9"},{"1010","a"},{"1011","b"},{"1100","c"},{ "1101","d" },{ "1110","e" },{ "1111","f" } };
	while (bit != "")
	{
		r += table[bit.substr(0, 4)];
		bit.erase(0, 4);
	}
	return r;
}
string leftShift(string s, int value)
{
	string temp = s.substr(0, value);
	for (int i = value; i < s.size(); i++)
		s[i - value] = s[i];
	s.erase(s.size() - value, value);
	s += temp;
	return s;
}
